function applyLang(lang) {
  if (!I18N[lang]) return;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    var val = I18N[lang][key];
    if (val == null) return;
    if (el.hasAttribute('data-i18n-html')) { el.innerHTML = val; }
    else { el.textContent = val; }
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-aria');
    var val = I18N[lang][key];
    if (val != null) el.setAttribute('aria-label', val);
  });
  document.querySelectorAll('.lang-toggle button').forEach(function (btn) {
    btn.classList.toggle('is-active', btn.getAttribute('data-lang') === lang);
  });
  try { localStorage.setItem('suspiros-lang', lang); } catch (err) {}
}

document.addEventListener('DOMContentLoaded', function () {
  document.querySelectorAll('.lang-toggle button').forEach(function (btn) {
    btn.addEventListener('click', function () { applyLang(btn.getAttribute('data-lang')); });
  });

  var forced = document.documentElement.getAttribute('data-force-lang');
  var initialLang;
  if (forced && I18N[forced]) {
    initialLang = forced;
  } else {
    var savedLang = null;
    try { savedLang = localStorage.getItem('suspiros-lang'); } catch (err) {}
    initialLang = savedLang || (navigator.language && navigator.language.slice(0, 2) === 'es' ? 'es' : 'en');
  }
  applyLang(initialLang);
});
